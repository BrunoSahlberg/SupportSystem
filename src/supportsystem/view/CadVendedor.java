    private void btnCadVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadVendedorActionPerformed
        try {
            Vendedor vendedor = new Vendedor();
            VendedorDAO vendedorDao = new VendedorDAO();
            
            vendedor.setNome_vendedor(cbxNomeVendedor.getText());
            vendedor.setPc_comissao(Integer.parseInt(cbxPcComissao.getText()));
            
            vendedorDao.inserirVendedor(vendedor);
            JOptionPane.showMessageDialog(null, "Vendedor inserido com sucesso!");

        } catch (SQLException ex) {
            LogController.createLog("Erro ao cadastrar Vendedor", "W");
        }
        LogController.createLog("Vendedor cadastrado", "I");
    }//GEN-LAST:event_btnCadVendedorActionPerformed

