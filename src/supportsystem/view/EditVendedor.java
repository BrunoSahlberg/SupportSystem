    private void btnAlterarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarProdutoActionPerformed
        try {
            Vendedor vendedor = new Vendedor();
            VendedorDAO vendedordao = new VendedorDAO();

            vendedor.setId_vendedor(vendedorGeral.getId_vendedor());
            vendedor.setNome_vendedor(cbxNomeVendedor.getText());
            vendedor.setPc_comissao(Integer.parseInt(cbxComissaoVendedor.getText()));

            vendedordao.alterarVendedor(vendedor);
            JOptionPane.showMessageDialog(null, "Vendedor alterado com sucesso!");

        } catch (SQLException ex) {
            LogController.createLog("Erro ao alterar o vendedor" + ex.getMessage(), "I");
        }
        LogController.createLog("Vendedor alterado", "I");
    }//GEN-LAST:event_btnAlterarProdutoActionPerformed

